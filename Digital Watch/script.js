


let h1kiId = document.querySelector("#time");


let h1kiId2 = document.querySelector("#Minets");


let h1kiId3 = document.querySelector("#Hover");


let h1kiId4 = document.querySelector("#ms");


const secondTimeTells = () => {

    let sec = new Date();

    let timelo = sec.getSeconds();

    let minet = sec.getMinutes();

    let hov = sec.getHours();

    let mss = sec.getMilliseconds();

    h1kiId.innerHTML = timelo;

    h1kiId2.innerHTML = minet;

    h1kiId3.innerHTML = hov;

    h1kiId4.innerHTML = mss;

}




setInterval(secondTimeTells, 700);
















































































































































































